void GetCRC(uint8_t *data, int len) {
  short index; /* will index into CRC lookup table */
  while (len--) {
    index = (*((char*)&CRC16+1)) ^ *(data++);
    (*((char*)&CRC16+1)) = (*((char*)&CRC16)) ^ auchCRCHi[index];
    (*((char*)&CRC16)) = auchCRCLo[index];
    }
}


